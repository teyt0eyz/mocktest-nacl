"""Hand-authored question bank for the NACL mock exam rebuild.

Convention: the CORRECT option is written FIRST (answer index 0). The build
script shuffles option order at assembly time, so authoring order is irrelevant
to the exam-taker. This keeps every entry easy to eyeball for correctness.

Schema per item:
  mc(subtopic, question, [correct, d1, d2, d3, d4])
  ms(subtopic, question, [opt...], n_correct)   -> first n_correct are correct
  wr(subtopic, question, answer_str)
"""

def mc(sub, q, opts):
    assert len(opts) == 5, q
    return {'type': 'mc', 'subtopic': sub, 'question': q,
            'options': list(opts), 'answer': 0, 'explanation': ''}

def ms(sub, q, opts, n_correct):
    return {'type': 'ms', 'subtopic': sub, 'question': q,
            'options': list(opts), 'answer': list(range(n_correct)),
            'explanation': ''}

def wr(sub, q, ans):
    return {'type': 'written', 'subtopic': sub, 'question': q,
            'answer': ans, 'explanation': ''}


